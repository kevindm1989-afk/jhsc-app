// ───────────────────────────────────────────────────────────────────────────
// FakeKeyServer — a stateful model of the migration-0007 RPCs the composition
// orchestrates: epoch table (live = rotated_at IS NULL), the committee_key_wraps
// table, member identities, and the rotation records. Faithful to the contract
// pins in ADR-0030 Amendment B (the file:line notes in decisions.md/threat-model).
// ───────────────────────────────────────────────────────────────────────────
interface EpochRow {
  key_id: string;
  epoch: number;
  rotated_at: number | null;
}
interface WrapRow {
  member: string;
  key_id: string;
  sealed: Uint8Array;
  rotation_id: string | null;
}
interface RotationRow {
  rotation_id: string;
  new_key_id: string;
  started: boolean;
  finalized: boolean;
}
type Wire = { status: number; body: unknown };
function wireOk(data: unknown): Wire {
  return { status: 200, body: { ok: true, data } };
}
function wireErr(reason: string, status: number): Wire {
  return { status, body: { ok: false, error: reason } };
}

class FakeKeyServer {
  epochs: EpochRow[] = [];
  wraps: WrapRow[] = [];
  identities = new Map<string, MemberFixture>();
  rotations = new Map<string, RotationRow>();
  /** The JWT subject the self-scoped ops (get_all_key_wraps / get_key_wrap /
   *  committee_key_state) resolve against. Default the actor; resume tests
   *  point it at the resumer. */
  selfUserId = ACTOR;
  /** ≥1-active-member guard input (rotate refuses when < 1). */
  activeMemberCount = 3;
  /** Per-target pubkey substitution (R2 self-detect): member → substitute keypair. */
  substitutePubkey = new Map<string, { pub: Uint8Array; fingerprint: string }>();
  #seq = 100;
  #epochN = 1;
  #rotN = 1;

  liveEpoch(): EpochRow | undefined {
    return this.epochs.find((e) => e.rotated_at === null);
  }
  seedEpoch(key_id: string, epoch: number, rotated_at: number | null): void {
    this.epochs.push({ key_id, epoch, rotated_at });
  }
  seedWrap(member: string, key_id: string, sealed: Uint8Array): void {
    this.wraps.push({ member, key_id, sealed, rotation_id: null });
  }
  wrapsOf(member: string): WrapRow[] {
    return this.wraps.filter((w) => w.member === member);
  }
  wrapOn(member: string, key_id: string): WrapRow | undefined {
    return this.wraps.find((w) => w.member === member && w.key_id === key_id);
  }

  /** rotate_committee_data_key — retire live, mint epoch+1, start a rotation. */
  rotate(): Wire {
    if (this.activeMemberCount < 1) return wireErr('no_active_members', 400); // :583-585
    const live = this.liveEpoch();
    if (live) live.rotated_at = ++this.#seq; // :588-592 marks prev rotated_at
    const new_key_id = `k-epoch-${++this.#epochN}`;
    const epoch = (live?.epoch ?? OLD_EPOCH) + 1;
    this.epochs.push({ key_id: new_key_id, epoch, rotated_at: null }); // :595-596
    const rotation_id = `rot-${this.#rotN++}`;
    this.rotations.set(rotation_id, { rotation_id, new_key_id, started: true, finalized: false });
    return wireOk({ rotation_id, new_key_id });
  }

  /** Concurrency R1: a SECOND rotate landing during the first's client tail —
   *  retires whatever is currently live (the first ceremony's new epoch). */
  concurrentRotate(): void {
    const live = this.liveEpoch();
    if (live) live.rotated_at = ++this.#seq;
    const new_key_id = `k-epoch-${++this.#epochN}`;
    const epoch = (live?.epoch ?? OLD_EPOCH) + 1;
    this.epochs.push({ key_id: new_key_id, epoch, rotated_at: null });
  }

  /** revoke_committee_member — archive→DELETE the removed member's wraps across
   *  ALL epochs (:699-706). Does NOT touch membership. */
  revoke(removed: string): Wire {
    const before = this.wraps.length;
    this.wraps = this.wraps.filter((w) => w.member !== removed);
    return wireOk({ wraps_removed: before - this.wraps.length });
  }

  /** wrap_committee_data_key_for_member — active-target gate (:502-503) +
   *  key-exists gate + ON CONFLICT (user_id,key_id) DO NOTHING (:509-511). */
  wrapMember(member: string, key_id: string, sealedHex: string, rotation_id: string | null): Wire {
    const id = this.identities.get(member);
    if (!id || !id.active) return wireErr('rls_denied', 403); // :502-503 active-target gate
    if (!this.epochs.some((e) => e.key_id === key_id)) return wireErr('not_found', 404);
    if (this.wrapOn(member, key_id)) return wireOk(null); // ON CONFLICT DO NOTHING (idempotent)
    this.wraps.push({ member, key_id, sealed: pgHexToBytes(sealedHex), rotation_id });
    return wireOk(null);
  }

  /** get_all_committee_key_wraps_for_self — every self wrap across live+retired. */
  getAllWraps(): Wire {
    const rows = this.wrapsOf(this.selfUserId).map((w) => {
      const e = this.epochs.find((x) => x.key_id === w.key_id)!;
      return {
        key_id: w.key_id,
        epoch: e.epoch,
        wrapped_ciphertext_hex: bytesToPgHex(w.sealed),
        is_live: e.rotated_at === null
      };
    });
    return wireOk(rows);
  }

  /** get_committee_key_wrap_for_self — the self LIVE-epoch wrap (step-1 unwrap). */
  getKeyWrap(): Wire {
    const live = this.liveEpoch();
    if (!live) return wireOk(null);
    const w = this.wrapOn(this.selfUserId, live.key_id);
    if (!w) return wireOk(null);
    return wireOk({
      key_id: live.key_id,
      epoch: live.epoch,
      wrapped_ciphertext_hex: bytesToPgHex(w.sealed)
    });
  }

  /** committee_key_state_for_self — live metadata + actor_has_wrap. */
  keyState(): Wire {
    const live = this.liveEpoch();
    if (!live) return wireOk(null);
    const wrapCount = this.wraps.filter((w) => w.key_id === live.key_id).length;
    return wireOk({
      key_id: live.key_id,
      epoch: live.epoch,
      wrap_count: wrapCount,
      actor_has_wrap: !!this.wrapOn(this.selfUserId, live.key_id)
    });
  }

  /** get_member_pubkey — enrolled+active target → {pubkey, fingerprint};
   *  else member_not_enrolled (F-174 closed collapse). Honors substitution. */
  memberPubkey(target: string): Wire {
    const sub = this.substitutePubkey.get(target);
    if (sub) return wireOk({ public_key_hex: bytesToPgHex(sub.pub), fingerprint: sub.fingerprint });
    const id = this.identities.get(target);
    if (!id || !id.active) return wireErr('member_not_enrolled', 403);
    return wireOk({ public_key_hex: bytesToPgHex(id.pub), fingerprint: id.fingerprint });
  }

  /** finalize_committee_data_key_rotation — pair against .started, and the new
   *  key must be UN-rotated-out (:650-653 → the R1 `invalid_new_key` case). */
  finalize(rotation_id: string, new_key_id: string): Wire {
    const r = this.rotations.get(rotation_id);
    if (!r || !r.started || r.finalized) return wireErr('rotation_not_started', 409);
    const e = this.epochs.find((x) => x.key_id === new_key_id);
    if (!e || e.rotated_at !== null) return wireErr('invalid_new_key', 409); // :650-653
    r.finalized = true;
    return wireOk(null);
  }
}

// ───────────────────────────────────────────────────────────────────────────
// Deterministic transport with op hooks. `onOp` fires SIDE-EFFECTS (session-end
// wipe / concurrent rotate) synchronously inside a chosen op+occurrence; `override`
// forces a fault (error body) or `'throw'`. No timers; the injection point is the
// op the composition is suspended on.
// ───────────────────────────────────────────────────────────────────────────
type OverrideResult = Wire | 'throw' | undefined;
interface TransportHooks {
  onOp?: (op: string, occurrence: number, body: Record<string, unknown>) => void;
  override?: (op: string, occurrence: number, body: Record<string, unknown>) => OverrideResult;
}
interface RecordedOp {
  op: string;
  occurrence: number;
  body: Record<string, unknown>;
}
function makeTransport(
  srv: FakeKeyServer,
  hooks: TransportHooks = {}
): { transport: T07OpTransport; ops: RecordedOp[] } {
  const ops: RecordedOp[] = [];
  const counts: Record<string, number> = {};
  const transport: T07OpTransport = async (body) => {
    const op = String(body.op);
    const occurrence = counts[op] ?? 0;
    counts[op] = occurrence + 1;
    ops.push({ op, occurrence, body });
    hooks.onOp?.(op, occurrence, body);
    const ov = hooks.override?.(op, occurrence, body);
    if (ov === 'throw') throw new Error(`injected transport throw on ${op}#${occurrence}`);
    if (ov) return ov;
    switch (op) {
      case 'rotate':
        return srv.rotate();
      case 'revoke_member':
        return srv.revoke(String(body.removed_member_id));
      case 'wrap_member':
        return srv.wrapMember(
          String(body.member_user_id),
          String(body.key_id),
          String(body.wrapped_ciphertext_hex),
          (body.rotation_id as string | null) ?? null
        );
      case 'get_all_key_wraps':
        return srv.getAllWraps();
      case 'get_key_wrap':
        return srv.getKeyWrap();
      case 'committee_key_state':
        return srv.keyState();
      case 'get_member_pubkey':
        return srv.memberPubkey(String(body.target_user_id));
      case 'finalize_rotate':
        return srv.finalize(String(body.rotation_id), String(body.new_key_id));
      default:
        throw new Error(`makeTransport: unexpected op ${op}`);
    }
  };
  return { transport, ops };
}

// ───────────────────────────────────────────────────────────────────────────
// Standard committee fixture: an old live epoch (k-epoch-1) with self + M1 + M2
// + REMOVED wraps; ACTOR/M1/M2 active, REMOVED inactive (membership already
// flipped by F182-6 before the composition runs).
// ───────────────────────────────────────────────────────────────────────────
interface Committee {
  srv: FakeKeyServer;
  localIdentity: BrowserLocalIdentityStore;
  actor: MemberFixture;
  m1: MemberFixture;
  m2: MemberFixture;
  removed: MemberFixture;
  oldKey: Uint8Array;
}
async function setupCommittee(): Promise<Committee> {
  const srv = new FakeKeyServer();
  srv.seedEpoch(OLD_KEY_ID, OLD_EPOCH, null);
  const localIdentity = silentStore();

  const actor = await makeMember(ACTOR, true);
  const m1 = await makeMember(M1, true);
  const m2 = await makeMember(M2, true);
  const removed = await makeMember(REMOVED, false);
  for (const m of [actor, m1, m2, removed]) srv.identities.set(m.user_id, m);
  await localIdentity.storeIdentityPrivateKey(ACTOR, actor.priv);

  // Old-epoch wraps (each member sealed the old key under their own pubkey).
  const oldKey = sodium.randombytes_buf(sodium.crypto_secretbox_KEYBYTES);
  srv.seedWrap(ACTOR, OLD_KEY_ID, sodium.crypto_box_seal(oldKey, actor.pub));
  srv.seedWrap(M1, OLD_KEY_ID, sodium.crypto_box_seal(oldKey, m1.pub));
  srv.seedWrap(M2, OLD_KEY_ID, sodium.crypto_box_seal(oldKey, m2.pub));
  srv.seedWrap(REMOVED, OLD_KEY_ID, sodium.crypto_box_seal(oldKey, removed.pub));
  srv.activeMemberCount = 3;
  return { srv, localIdentity, actor, m1, m2, removed, oldKey };
}

