  // -------------------------------------------------------------------------
  it('FINDING 2 (MEDIUM): a revoke_member failure AFTER a landed rotate must carry the orphan context (orphaned + rotation_id + new_key_id), not a bare failed that strands the zero-wrap epoch', async () => {
    const c = await setupCommittee();
    // rotate succeeds (mints a fresh live zero-wrap epoch); revoke_member then
    // fails with a hard server fault.
    const hooks: TransportHooks = {
      override: (op) => (op === 'revoke_member' ? wireErr('revoke_post_failed', 500) : undefined)
    };
    const { transport, ops } = makeTransport(c.srv, hooks);
    const client = new SupabaseT07Client({ transport, localIdentity: c.localIdentity });
    const holder = new CommitteeKeyHolder();
    holder.set({ data_key: c.oldKey, key_id: OLD_KEY_ID, epoch: OLD_EPOCH });

    const r = await runRotation(standardOpts(c, client, holder));

    // Determinism / precondition — the rotate landed (minting the orphan epoch)
    // and the revoke was the failing op.
    expect(ops.some((o) => o.op === 'rotate'), 'the rotate landed before revoke failed').toBe(true);
    expect(ops.some((o) => o.op === 'revoke_member'), 'the revoke_member op was dispatched').toBe(
      true
    );
    const orphanEpoch = c.srv.liveEpoch()!.key_id; // the new zero-wrap live epoch
    expect(orphanEpoch, 'rotate minted a fresh live epoch (the orphan)').not.toBe(OLD_KEY_ID);

    // The result must carry the orphan context so the caller can retire the
    // zero-wrap epoch and re-drive — a bare `failed` strands it.
    expect(
      r.status,
      'FINDING 2: a revoke-fail after a landed rotate strands a zero-wrap orphan epoch — it must be reported orphaned, not a bare failed'
    ).toBe('orphaned');
    expect(
      r.rotation_id,
      'FINDING 2: orphaned must carry rotation_id so the caller can route to abandon+re-drive'
    ).toBeDefined();
    expect(
      r.new_key_id,
      'FINDING 2: orphaned must carry new_key_id (the orphan epoch) so the caller can retire it'
    ).toBe(orphanEpoch);

    // No self-wrap / no finalize was attempted on the orphan epoch.
    expect(ops.some((o) => o.op === 'wrap_member'), 'no wrap POSTed after a failed revoke').toBe(
      false
    );
    expect(
      ops.some((o) => o.op === 'finalize_rotate'),
      'no finalize on a zero-wrap orphan epoch'
    ).toBe(false);

    // Documentation of the orphan state: the failed revoke left the removed
    // member's old-epoch wrap in place — the orphan context is what lets the
    // caller drive the purge on re-drive.
    expect(
      c.srv.wrapOn(REMOVED, OLD_KEY_ID),
      'the failed revoke left the removed member`s old-epoch wrap un-purged (orphan state the caller must clean up)'
    ).toBeDefined();
  });

  // -------------------------------------------------------------------------
  // FINDING 3 (MEDIUM) — no fail-before-mutate on a malformed actor_public_key.
  // The sibling init guards `actor_public_key.length !== 32` BEFORE every
