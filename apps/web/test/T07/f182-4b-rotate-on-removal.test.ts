  // RE-REVIEW FINDING B (fail-loud consistency) — a session-end wipe landing
  // AFTER the last re-wrap but BEFORE `finalize` still reports `ok` on a wiped
  // ceremony. The composition re-checks the ENTRY `entryGen` at the step-5 install
  // (:1221) but NOT again before the step-7 finalize (:1303): a wipe landing in
  // the pre-finalize window is never detected, so a wiped ceremony finalizes and
  // returns a clean `ok`.
  //
  // The wipe is injected during the LAST remaining re-wrap's POST await (M2 — the
  // final entry in remaining_members) — strictly AFTER every re-wrap has sealed +
  // landed, strictly BEFORE `finalize` is dispatched (proven by
  // finalizeSeenBeforeWipe===false). A wipe DURING the `finalize` RPC await itself
  // is acceptable-honest (the rotation completed server-side) and is deliberately
