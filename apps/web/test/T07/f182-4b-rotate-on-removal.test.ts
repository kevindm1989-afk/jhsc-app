// Open the actor's self-wrap on the new epoch → recover the fresh new key bytes.
function recoverNewKey(srv: FakeKeyServer, actor: MemberFixture, newKeyId: string): Uint8Array {
  const w = srv.wrapOn(ACTOR, newKeyId);
  if (!w) throw new Error('no self-wrap on the new epoch to recover the new key from');
  return sodium.crypto_box_seal_open(w.sealed, actor.pub, actor.priv);
}
// The new_key_id the composition minted == the key_id it self-wrapped under.
function newKeyIdFrom(ops: RecordedOp[]): string {
  const selfWrap = ops.find((o) => o.op === 'wrap_member');
  if (!selfWrap) throw new Error('no wrap_member op recorded');
  return String(selfWrap.body.key_id);
}
function opIndex(ops: RecordedOp[], pred: (o: RecordedOp) => boolean): number {
  return ops.findIndex(pred);
}

// A secretbox record sealed under a data key — the "existing data" probe.
interface Record0 {
  nonce: Uint8Array;
  ct: Uint8Array;
  pt: string;
}
function sealRecord(pt: string, key: Uint8Array): Record0 {
  const nonce = sodium.randombytes_buf(sodium.crypto_secretbox_NONCEBYTES);
  const ct = sodium.crypto_secretbox_easy(new Uint8Array(new TextEncoder().encode(pt)), nonce, key);
  return { nonce, ct, pt };
}
async function tryReadRecord(holder: CommitteeKeyHolder, rec: Record0): Promise<string | null> {
  const r = await holder.trialOpen<string>((k) => {
    const pt = sodium.crypto_secretbox_open_easy(rec.ct, rec.nonce, k); // throws on wrong key
    return new TextDecoder().decode(pt);
  });
  return r.status === 'ok' ? r.value : null;
}

